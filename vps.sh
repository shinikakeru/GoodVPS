read -r USER_SSH_KEY

if [ -z "$USER_SSH_KEY" ]; then
    echo "Ошибка: ключ не введен!"
    exit 1
fi

mkdir -p /root/.ssh
chmod 700 /root/.ssh
echo "$USER_SSH_KEY" >> /root/.ssh/authorized_keys
chmod 600 /root/.ssh/authorized_keys
echo "Ключ успешно добавлен!"

